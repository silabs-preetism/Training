uint8_t final_input_data[2000];
uint16_t total_msg_len;

int32_t pad_sha3_shake(uint8_t pad_char, uint8_t mode)
{
  uint32_t m;
  uint16_t i, r, q, c, msg_size;
  m = sizeof(input_data) / sizeof(input_data[0]);
  if (mode == SHA3_512) {
    c = 512 * 2;
  } else if (mode == SHA3_384) {
    c = 384 * 2;
  } else if (mode == SHA3_256 || mode == SHAKE_256) {
    c = 256 * 2;
  } else if (mode == SHA3_224) {
    c = 224 * 2;
  } else if (mode == SHAKE_128) {
    c = 128 * 2;
  } else {
    return RSI_ERROR_INVALID_PARAM;
  }

  r             = 1600 - c;
  q             = (r / 8) - ((m) % (r / 8));
  msg_size      = (m + (q - 2) + 2);
  total_msg_len = msg_size;

  if (q == 1) // (M || 0x86)
  {
    for (i = 0; i < (m + 1); i++) {
      if (i < m)
        final_input_data[i] = input_data[i];
      else {
        if (pad_char == 0x06)
          final_input_data[i] = 0x86;
        else
          final_input_data[i] = 0x9F;
      }
    }
  } else if (q == 2) // (M || 0x06 || 0x80)
  {
    for (i = 0; i < (m + 2); i++) {
      if (i < m)
        final_input_data[i] = input_data[i];
      else if (i == m)
        final_input_data[i] = pad_char;
      else
        final_input_data[i] = 0x80;
    }
  } else //q>2 and (M || 0x06 || 0x00.. || 0x80)
  {
    for (i = 0; i < (msg_size); i++) {
      if (i < m)
        final_input_data[i] = input_data[i];
      else if (i == m)
        final_input_data[i] = pad_char;
      else if (i > m && i < (msg_size - 1))
        final_input_data[i] = 0;
      else
        final_input_data[i] = 0x80;
    }
  }
  return 0;
}

//! Function to calculate sha3 digest
int32_t rsi_crypto_sha3_app()
{
  int32_t status = RSI_SUCCESS;
  //Buffers to store responses
  uint8_t digest[SHA_512_DIGEST_LEN];

  //! WC initialization
  status = rsi_wireless_init(0, 0);
  if (status != RSI_SUCCESS) {
    return status;
  }

  //! Memset before filling
  memset(digest, 0, SHA_512_DIGEST_LEN);

  //!Padding of SHA3/SHAKE with mode
  status = pad_sha3_shake(PAD_CHAR_SHA3, SHA3_512);
  if (status != RSI_SUCCESS) {
#ifdef RSI_ENABLE_DEBUG_PRINT
    printf("\n SHA3 Padding Failed 0x%x \n", status);
#endif
    return status;
  }

  //!To compute digest using SHA3 or SHAKE
  status = rsi_sha3(SHA3_512, final_input_data, total_msg_len, digest);
  if (status != RSI_SUCCESS) {
#ifdef RSI_ENABLE_DEBUG_PRINT
    printf("\n SHA3 Failed 0x%x \n", status);
#endif
    return status;
  }
#ifdef RSI_ENABLE_DEBUG_PRINT
  printf("\n SHA3 success\n");
  printf("\n\n\nDone\n\n\n");
#endif
  return 0;
}

