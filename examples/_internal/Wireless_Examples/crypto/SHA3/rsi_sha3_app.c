int main()
{
  int32_t status;

#ifdef RSI_WITH_OS
  rsi_task_handle_t sha3_task_handle = NULL;

  rsi_task_handle_t driver_task_handle = NULL;
#endif

  //! Driver initialization
  status = rsi_driver_init(global_buf, GLOBAL_BUFF_LEN);
  if ((status < 0) || (status > GLOBAL_BUFF_LEN)) {
    return status;
  }

  //! RS9117 intialisation
  status = rsi_device_init(LOAD_NWP_FW);
  if (status != RSI_SUCCESS) {
    return status;
  }

#ifdef RSI_WITH_OS
  //! OS case
  //! Task created for SHA3 task
  rsi_task_create(rsi_sha3_encry_decry_app,
                  "sha3_task",
                  RSI_SHA3_TASK_STACK_SIZE,
                  NULL,
                  RSI_SHA3_TASK_PRIORITY,
                  &sha3_task_handle);

  //! Task created for Driver task
  rsi_task_create(rsi_wireless_driver_task,
                  "driver_task",
                  RSI_DRIVER_TASK_STACK_SIZE,
                  NULL,
                  RSI_DRIVER_TASK_PRIORITY,
                  &driver_task_handle);

  //! OS TAsk Start the scheduler
  rsi_start_os_scheduler();

#else
  //! NON - OS case
  //! Call SHA3 digest computation application
  status = rsi_crypto_sha3_app();

  //! Application main loop
  main_loop();
#endif
  return status;
}
